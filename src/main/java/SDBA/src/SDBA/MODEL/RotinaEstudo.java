package SDBA.src.SDBA.MODEL;

public class RotinaEstudo {

    private int idaluno;
    private int horasSono;
    private int horasEstudo;
    private int pausas;
    private int tempoLazer;
    private int nivelEstresse;

    public int getIdaluno() {
        return idaluno;
    }

    public void setIdaluno(int idaluno) {
        this.idaluno = idaluno;
    }

    public int getHorasSono() {
        return horasSono;
    }

    public void setHorasSono(int horasSono) {
        this.horasSono = horasSono;
    }

    public int getHorasEstudo() {
        return horasEstudo;
    }

    public void setHorasEstudo(int horasEstudo) {
        this.horasEstudo = horasEstudo;
    }

    public int getPausas() {
        return pausas;
    }

    public void setPausas(int pausas) {
        this.pausas = pausas;
    }

    public int getTempoLazer() {
        return tempoLazer;
    }

    public void setTempoLazer(int tempoLazer) {
        this.tempoLazer = tempoLazer;
    }

    public int getNivelEstresse() {
        return nivelEstresse;
    }

    public void setNivelEstresse(int nivelEstresse) {
        this.nivelEstresse = nivelEstresse;
    }
}
